
# Lista de str

students = ['Marta', 'Claudia', 'Romeo', 'Albora', 'Vladimir', 'Diana']

print(students[0]) # Marta
print(students[1]) # Claudia
print(students[2]) # Romeo

# a la inversa, empieza por el final:
print(students[-1]) # Diana
print(students[-2]) # Vladimir
print(students[-3]) # Albora

