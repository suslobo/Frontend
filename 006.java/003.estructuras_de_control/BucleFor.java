
public class BucleFor {

    public static void main(String[] args) {
        
        // Operador incremento ++
        // Operador decremento --

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // for (start; stop; step)
        for (int i = 0; i < 20; i += 2) {
            System.out.println(i);
        }

        // de 20 a 0, a la inversa, decrementando

        for (int i = 20; i > 0; i--) {
            System.out.println(i);
        }

    }
    
}
